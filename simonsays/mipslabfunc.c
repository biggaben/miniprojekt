// quicksleep:
void quicksleep(int cyc) {
	int i;
	for(i = cyc; i > 0; i--);
}

int flipped_switch(){
  int flipped = getsw();
  switch(flipped){
    case 4: return 1;
    case 2: return 2;
    case 1: return 3;
    default: return 0;
  }
}

